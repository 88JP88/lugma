            <p>La plataforma ofrece diferentes tipos de perfiles de usuario, cada uno con características y funciones específicas adaptadas a sus necesidades. Estos perfiles son:</p>

    <h2>Estudiantes</h2>
    <p>Los estudiantes tienen acceso a un conjunto de herramientas diseñadas para apoyar su proceso de aprendizaje. Pueden acceder a materiales de estudio, realizar tareas, participar en foros de discusión y gestionar su progreso académico de manera personalizada. No es necesario estar asociado a una institución para utilizar la plataforma como estudiante.</p>

    <h2>Profesores</h2>
    <p>Los profesores cuentan con herramientas especiales para administrar sus cursos, crear y entregar materiales de enseñanza, evaluar a los estudiantes y comunicarse con ellos de manera efectiva. Pueden organizar horarios, gestionar tareas y tener un seguimiento del desempeño académico de sus estudiantes. Al igual que los estudiantes, los profesores pueden utilizar la plataforma de manera independiente o en el contexto de una institución.</p>

    <h2>Coordinadores</h2>
    <p>Los coordinadores tienen funciones adicionales que les permiten gestionar y supervisar varios cursos, asignar profesores a grupos específicos, realizar un seguimiento del progreso de los estudiantes en diferentes programas académicos y generar informes. También pueden establecer políticas y directrices para el uso de la plataforma en su institución.</p>

    <h2>Perfil Externo</h2>
    <p>El perfil externo está diseñado para usuarios que desean utilizar las herramientas generales de la plataforma sin estar asociados a una institución educativa específica. Pueden aprovechar las funcionalidades básicas para administrar su información personal, acceder a recursos y utilizar las herramientas disponibles para su desarrollo y crecimiento personal.</p>

    <p>Es importante destacar que cada perfil de usuario tiene características y funciones independientes y diferentes a los otros perfiles. Sin embargo, existen elementos compartidos y la posibilidad de utilizar la plataforma tanto a nivel personalizado como a nivel más amplio con una institución o grupo específico. La plataforma ofrece flexibilidad para adaptarse a las necesidades de los usuarios y promover un entorno de aprendizaje colaborativo.</p>

