            <h3>Herramientas</h3>
            <p>El módulo de Herramientas es una parte fundamental de nuestra plataforma. Está diseñado para brindar funcionalidades útiles y prácticas tanto en el ámbito educativo como personal.</p>

<p>Este módulo cuenta con las siguientes características:</p>

<h2>Horarios</h2>
<p>El módulo de Horarios permite a los usuarios tener acceso a sus horarios personalizados, facilitando la gestión del tiempo y la organización de actividades.</p>

<h2>Tareas</h2>
<p>La función de Tareas permite recibir y entregar asignaciones, eternas como tareas personales de manera digital, simplificando la administración y el seguimiento de las tareas asignadas.</p>

<h2>Alertas</h2>
<p>El módulo de Alertas brinda información importante y actualizada sobre eventos relevantes, anuncios y notificaciones del sistema, como tambien la personalización de las alertas según el caso.</p>

<h2>Grupos</h2>
<p>La función de Grupos facilita la colaboración y el trabajo en equipo, permitiendo crear grupos de estudio, proyectos o discusión, públicos y privados, con el fin de promover la discución e interacción entre los usuarios.</p>

<h2>Correo</h2>
<p>El módulo de Correo proporciona una bandeja de entrada personalizada para enviar y recibir mensajes de forma segura con otros usuarios internamente.</p>

<p>El objetivo principal de este módulo de Herramientas es brindar una experiencia completa y accesible para mejorar tanto la experiencia educativa como personal de los usuarios. Independientemente de su perfil, podrán utilizar estas herramientas de manera intuitiva y eficiente.</p>

